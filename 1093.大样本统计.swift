// @lc code=start
class Solution {
    func sampleStats(_ count: [Int]) -> [Double] {
        
        var maxVal = 0
        var minVal = -1
        var avg = 0.0
        var sum = 0
        var mode = 0
        var total = 0

        for i in 0..<count.count {
            if count[i] > 0 {
                maxVal = i
                if minVal < 0 { minVal = i }
                sum += i * count[i]
                if count[i] > count[mode] { mode = i }
                total += count[i]
            }
        }

        avg = Double(sum) / Double(total)

        var cnt = 0
        var mid = 0.0

        let m1 = (total + 1) >> 1
        let m2 = total >> 1 + 1

        for i in 0...255 {
            if cnt < m1 && cnt + count[i] >= m1 {
                mid += Double(i) / 2.0
            }

            if cnt < m2 && cnt + count[i] >= m2 {
                mid += Double(i) / 2.0
            }

            cnt += count[i]
        }

        return [Double(minVal), Double(maxVal), Double(avg), Double(mid), Double(mode)]
    }
}
